//Find the all odd numbers from 61 to 100:
let num = 61;

while(num<=100){
    if(num % 2 !=0){
        console.log("This is odd number:"+ num);
    }
    num++;
}
// Find the all even numbers from 61 to 100:
while(num<=100){
    if(num % 2 === 0){
        console.log("this is an even number" +num);
    }
    num++;
}